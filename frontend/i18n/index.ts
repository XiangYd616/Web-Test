const defaultLanguage =
  (typeof window !== 'undefined' && window.localStorage.getItem(LANGUAGE_STORAGE_KEY)) ||
  (typeof navigator !== 'undefined' ? navigator.language : 'zh-CN') ||
  'zh-CN';

void i18n.use(initReactI18next).init({
  resources,
  lng: defaultLanguage in resources ? defaultLanguage : 'zh-CN',
  fallbackLng: 'zh-CN',
  interpolation: {
    escapeValue: false,
  },
});

export const setAppLanguage = (language: string) => {
  const next = language in resources ? language : 'zh-CN';
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, next);
  }
  void i18n.changeLanguage(next);
};

